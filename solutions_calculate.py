import math


def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def reminder(a, b):
    return a % b


def square(a):
    return a * a


def ceil(a):
    return math.ceil(a)


def floor(a):
    return math.floor(a)
